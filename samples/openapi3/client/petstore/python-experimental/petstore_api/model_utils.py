def __check_numeric_validations(
        validations, input_values,
        _instantiation_metadata: InstantiationMetadata):

    if is_json_validation_enabled('multipleOf', _instantiation_metadata.configuration) and 'multiple_of' in validations:
        multiple_of_values = validations['multiple_of']
        for multiple_of_value in multiple_of_values:
            if (isinstance(input_values, Decimal) and
                not (float(input_values) / multiple_of_value).is_integer()
            ):
                # Note 'multipleOf' will be as good as the floating point arithmetic.
                raise_validation_error_message(
                    value=input_values,
                    constraint_msg="value must be a multiple of",
                    constraint_value=multiple_of_value,
                    path_to_item=_instantiation_metadata.path_to_item
                )

    checking_max_or_min_values = {'exclusive_maximum', 'inclusive_maximum', 'exclusive_minimum',
             'inclusive_minimum'}.isdisjoint(validations) is False
    if not checking_max_or_min_values:
        return
    max_val = input_values
    min_val = input_values

    if (is_json_validation_enabled('exclusiveMaximum', _instantiation_metadata.configuration) and
            'exclusive_maximum' in validations and
            max_val >= validations['exclusive_maximum']):
        raise_validation_error_message(
            value=input_values,
            constraint_msg="must be a value less than",
            constraint_value=validations['exclusive_maximum'],
            path_to_item=_instantiation_metadata.path_to_item
        )

    if (is_json_validation_enabled('maximum', _instantiation_metadata.configuration) and
            'inclusive_maximum' in validations and
            max_val > validations['inclusive_maximum']):
        raise_validation_error_message(
            value=input_values,
            constraint_msg="must be a value less than or equal to",
            constraint_value=validations['inclusive_maximum'],
            path_to_item=_instantiation_metadata.path_to_item
        )

    if (is_json_validation_enabled('exclusiveMinimum', _instantiation_metadata.configuration) and
            'exclusive_minimum' in validations and
            min_val <= validations['exclusive_minimum']):
        raise_validation_error_message(
            value=input_values,
            constraint_msg="must be a value greater than",
            constraint_value=validations['exclusive_maximum'],
            path_to_item=_instantiation_metadata.path_to_item
        )

    if (is_json_validation_enabled('minimum', _instantiation_metadata.configuration) and
            'inclusive_minimum' in validations and
            min_val < validations['inclusive_minimum']):
        raise_validation_error_message(
            value=input_values,
            constraint_msg="must be a value greater than or equal to",
            constraint_value=validations['inclusive_minimum'],
            path_to_item=_instantiation_metadata.path_to_item
        )


def check_validations(
        validations, input_values,
        _instantiation_metadata: InstantiationMetadata):
    """Raises an exception if the input_values are invalid

    Args:
        validations (dict): the validation dictionary for a specific schema or property
        input_values (list/str/int/float/date/datetime): the values that we
            are checking.
        _instantiation_metadata (InstantiationMetadata):
            contains Configuration which is needed to turn on checking validation features
            contains path_to_item which is used in the validation error message
    """
    if isinstance(input_values, str):
        __check_str_validations(validations, input_values, _instantiation_metadata)
    elif isinstance(input_values, list):
        __check_list_validations(validations, input_values, _instantiation_metadata)
    elif isinstance(input_values, dict):
        __check_dict_validations(validations, input_values, _instantiation_metadata)
    elif isinstance(input_values, Decimal):
        __check_numeric_validations(validations, input_values, _instantiation_metadata)


def get_type_error(var_value, path_to_item, valid_classes, key_type=False):
    error_msg = type_error_message(
        var_name=path_to_item[-1],
        var_value=var_value,
        valid_classes=valid_classes,
        key_type=key_type
    )
    return ApiTypeError(
        error_msg,
        path_to_item=path_to_item,
        valid_classes=valid_classes,
        key_type=key_type
    )


def deserialize_file(response_data, configuration, content_disposition=None):
    """Deserializes body to file

    Saves response body into a file in a temporary folder,
    using the filename from the `Content-Disposition` header if provided.

    Args:
        param response_data (str):  the file data to write
        configuration (Configuration): the instance to use to convert files

    Keyword Args:
        content_disposition (str):  the value of the Content-Disposition
            header

    Returns:
        (file_type): the deserialized file which is open
            The user is responsible for closing and reading the file
    """
    fd, path = tempfile.mkstemp(dir=configuration.temp_folder_path)
    os.close(fd)
    os.remove(path)

    if content_disposition:
        filename = re.search(r'filename=[\'"]?([^\'"\s]+)[\'"]?',
                             content_disposition).group(1)
        path = os.path.join(os.path.dirname(path), filename)

    with open(path, "wb") as f:
        if isinstance(response_data, str):
            # change str to bytes so we can write it
            response_data = response_data.encode('utf-8')
        f.write(response_data)

    f = open(path, "rb")
    return f


def type_error_message(var_value=None, var_name=None, valid_classes=None,
                       key_type=None):
    """
    Keyword Args:
        var_value (any): the variable which has the type_error
        var_name (str): the name of the variable which has the typ error
        valid_classes (tuple): the accepted classes for current_item's
                                  value
        key_type (bool): False if our value is a value in a dict
                         True if it is a key in a dict
                         False if our item is an item in a list
    """
    key_or_value = 'value'
    if key_type:
        key_or_value = 'key'
    valid_classes_phrase = __get_valid_classes_phrase(valid_classes)
    msg = (
        "Invalid type. Required {1} type {2} and "
        "passed type was {3}".format(
            var_name,
            key_or_value,
            valid_classes_phrase,
            type(var_value).__name__,
        )
    )
    return msg


def __get_valid_classes_phrase(input_classes):
    """Returns a string phrase describing what types are allowed
    """
    all_classes = list(input_classes)
    all_classes = sorted(all_classes, key=lambda cls: cls.__name__)
    all_class_names = [cls.__name__ for cls in all_classes]
    if len(all_class_names) == 1:
        return 'is {0}'.format(all_class_names[0])
    return "is one of [{0}]".format(", ".join(all_class_names))


@lru_cache(maxsize=None)
def get_new_class(
    class_name: str,
    bases: typing.Tuple[typing.Type[typing.Union[Schema, typing.Any]], ...]
) -> typing.Type[Schema]:
    """
    Returns a new class that is made with the subclass bases
    """
    try:
        return type(class_name, bases, {})
    except TypeError as ex:
        enum_extension_error_message = 'Cannot extend enumerations'
        if str(ex) != enum_extension_error_message:
            print((class_name, bases))
            for base in bases:
                print((base, base.__mro__))
            raise ex

        all_classes = []
        key_value_pairs = []
        for base in bases:
            if issubclass(base, Enum):
                for enum_item in base:
                    key_value_pairs.append((str(enum_item.name), enum_item.value))
                enum_bases_without_enum = base.__bases__[:-1]
                all_classes.extend(enum_bases_without_enum)
                continue
            all_classes.append(base)

        return get_new_enum(class_name, tuple(key_value_pairs), tuple(all_classes))


@lru_cache(maxsize=None)
def get_new_enum(
    class_name: str,
    key_value_pairs: typing.Tuple[typing.Tuple[str, typing.Any]],
    bases: typing.Tuple[typing.Type[typing.Union[Schema, typing.Any]], ...]
) -> typing.Type[Enum]:
    """
    Manufactures a new enum class
    """
    class_dict = _EnumDict()
    for key, value in key_value_pairs:
        class_dict[key] = value
    bases = list(bases)
    bases.append(Enum)
    bases = tuple(bases)
    return type(class_name, bases, class_dict)


