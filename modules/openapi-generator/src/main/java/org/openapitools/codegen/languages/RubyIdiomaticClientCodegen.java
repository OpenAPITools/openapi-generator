    @Override
    public String toApiFilename(String name) {
        String[] parts = name.split("/");
        List<String> out = new ArrayList<>();
        for (String p : parts) out.add(underscore(p.replace('-', '_')));
        // Join with '/' (not File.separator): the api sub-path must be identical on all
        // platforms so generated output and the FILES manifest match on Windows too.
        return String.join("/", out);
    }

    @Override
    public String toApiName(String name) {
        // HYBRID NAMESPACING (design spec decision #13, mirrors CrystalClientCodegen):
        // this produces the COMPACT resource leaf ("dcim/cable-terminations" ->
        // "Dcim::CableTerminations"). The api.mustache template nests moduleName + the
        // `Api` wrapper as `module` blocks, then declares `class <this>` compact.
        // Unlike Crystal, we deliberately do NOT prefix the api-namespace wrapper here
        // (Crystal prefixes then strips via x-cr-api-inner-class) -- keep this split.
        // The `Api` wrapper itself is templated via the `apiNamespace` CliOption (default
        // "Api", empty disables it) and applied purely in api.mustache; see API_NAMESPACE.
        String[] parts = name.split("/");
        List<String> out = new ArrayList<>();
        for (String p : parts) out.add(camelize(p.replace('-', '_')));
        return String.join("::", out);
    }

    @Override
    public String toEnumValue(String value, String datatype) {
        if ("Integer".equals(datatype) || "Float".equals(datatype)) {
            return value;
        }
        return "'" + escapeText(value).replace("'", "\\'") + "'";
    }

    // DefaultCodegen#getSchemaType returns the raw OAI type (e.g. "number", "integer") for
    // top-level (model-level) enum schemas, whereas CodegenProperty#dataType goes through
    // getTypeDeclaration/typeMapping and already yields "Integer"/"Float". Without this
    // override, model-level numeric enums (e.g. a schema whose type is `number`/`integer` with
    // an `enum:` list) would never match the toEnumValue check above and would stay quoted.
    // Mirrors RubyClientCodegen.getSchemaType.
    @Override
    public String getSchemaType(Schema schema) {
        String openAPIType = super.getSchemaType(schema);
        String type;
        if (typeMapping.containsKey(openAPIType)) {
            type = typeMapping.get(openAPIType);
            if (languageSpecificPrimitives.contains(type)) {
                return type;
            }
        } else {
            type = openAPIType;
        }
        if (type == null) {
            return null;
        }
        return toModelName(type);
    }

    @Override
    public void preprocessOpenAPI(OpenAPI openAPI) {
        super.preprocessOpenAPI(openAPI);
        Set<String> paths = openAPI.getPaths() == null
                ? Collections.emptySet() : openAPI.getPaths().keySet();
        this.resourceSegments = RubyApiRouting.resourceSegments(paths);
        this.apiBasePrefix = additionalProperties.containsKey("apiBasePath")
                ? stripSlashes((String) additionalProperties.get("apiBasePath"))
                : RubyApiRouting.commonBasePrefix(paths);
    }

    private static String stripSlashes(String s) {
        return s == null ? "" : s.replaceAll("^/+", "").replaceAll("/+$", "");
    }

    @Override
    public void addOperationToGroup(String tag, String resourcePath, Operation operation,
                                    CodegenOperation co, Map<String, List<CodegenOperation>> operations) {
        RubyApiRouting.Route r = RubyApiRouting.route(
                co.path, co.httpMethod, co.operationId, resourceSegments, apiBasePrefix);
        String groupKey = r.resource == null ? r.namespace : r.namespace + "/" + r.resource;
        co.operationId = toOperationId(r.action);
        co.vendorExtensions.put("x-rb-namespace", r.namespace);
        if (r.resource != null) co.vendorExtensions.put("x-rb-resource", r.resource);
        co.baseName = groupKey;
        List<CodegenOperation> opList = operations.computeIfAbsent(groupKey, k -> new ArrayList<>());
        // An operation carrying multiple tags is delivered once per tag; because we group by
        // PATH (not by tag), those extra deliveries would emit duplicate methods (a single
        // GET .../cluster becoming both `cluster` and `cluster_get`). A path+verb pair is
        // unique in an OpenAPI document, so if it is already present this is a re-delivery --
        // skip it.
        for (CodegenOperation existing : opList) {
            if (existing.path.equals(co.path) && existing.httpMethod.equals(co.httpMethod)) {
                return;
            }
        }
        // operationId uniqueness within the group (verb suffix on collision);
        // re-scan after each mutation so 2+ prior collisions are handled correctly.
        String unique = co.operationId;
        int counter = 0;
        boolean clash = uniqueClash(opList, unique);
        while (clash) {
            unique = co.operationId + "_" + co.httpMethod.toLowerCase(Locale.ROOT)
                     + (counter == 0 ? "" : "_" + counter);
            counter++;
            clash = uniqueClash(opList, unique);
        }
        if (!unique.equals(co.operationId)) {
            LOGGER.warn("ruby-idiomatic: action collision, renamed `{}` -> `{}`", co.operationId, unique);
            co.operationId = unique;
        }
        opList.add(co);
    }

    /** Returns true if any operation in {@code opList} already uses {@code candidate} as its operationId. */
    private static boolean uniqueClash(List<CodegenOperation> opList, String candidate) {
        for (CodegenOperation op : opList) {
            if (op.operationId.equals(candidate)) return true;
        }
        return false;
    }

    // Zeitwerk's default Inflector#camelize, reproduced to detect divergence:
    // basename.split('_').map { seg -> Character.toUpperCase(seg[0]) + seg.substring(1) }.join
    // Segments are already lowercase in file names, so acronyms lose their casing here.
    private static String zeitwerkCamelize(String filename) {
        StringBuilder b = new StringBuilder();
        for (String seg : filename.split("_")) {
            if (seg.isEmpty()) continue;
            b.append(Character.toUpperCase(seg.charAt(0))).append(seg.substring(1));
        }
        return b.toString();
    }

    // Last "::"-separated component of a (possibly qualified) constant name -- the leaf that
    // Zeitwerk resolves from a single file/dir basename. "DedicatedCloud::TwoFAWhitelist" ->
    // "TwoFAWhitelist"; a bare "Config" is returned unchanged.
    private static String lastConstantSegment(String className) {
        int i = className.lastIndexOf("::");
        return i < 0 ? className : className.substring(i + 2);
    }

    // A namespace accessor that would collide with the Client's own methods is suffixed
    // (e.g. a namespace named "connection" -> `client.connection_api`) so the real
    // configuration/connection accessors keep working.
    private static String safeAccessorName(String name) {
        return RESERVED_ACCESSOR_NAMES.contains(name) ? name + "_api" : name;
    }

    @Override
    @SuppressWarnings("unchecked")
    public Map<String, Object> postProcessSupportingFileData(Map<String, Object> objs) {
        Map<String, Map<String, Object>> nsMap = new TreeMap<>();
        Map<String, Set<String>> resourcesByNs = new TreeMap<>();
        Map<String, Object> apiInfo = (Map<String, Object>) objs.get("apiInfo");
        if (apiInfo != null) {
            List<Map<String, Object>> apis = (List<Map<String, Object>>) apiInfo.get("apis");
            if (apis != null) {
                for (Map<String, Object> api : apis) {
                    Map<String, Object> ops = (Map<String, Object>) api.get("operations");
                    if (ops == null) continue;
                    List<CodegenOperation> opList = (List<CodegenOperation>) ops.get("operation");
                    if (opList == null) continue;
                    for (CodegenOperation co : opList) {
                        String ns = (String) co.vendorExtensions.get("x-rb-namespace");
                        String res = (String) co.vendorExtensions.get("x-rb-resource");
                        if (ns == null) continue;
                        nsMap.computeIfAbsent(ns, k -> {
                            Map<String, Object> m = new HashMap<>();
                            String base = underscore(sanitizeName(ns.replace('-', '_')));
                            m.put("name", base);
                            // `accessor` is the Client method name; `name` stays the on-disk
                            // dir basename (Zeitwerk inflection key), so guarding one never
                            // desyncs the other.
                            m.put("accessor", safeAccessorName(base));
                            m.put("className", toApiName(ns));
                            return m;
                        });
                        if (res != null) resourcesByNs.computeIfAbsent(ns, k -> new TreeSet<>()).add(res);
                    }
                }
            }
        }
        List<Map<String, Object>> nsList = new ArrayList<>();
        for (Map.Entry<String, Map<String, Object>> e : nsMap.entrySet()) {
            Map<String, Object> m = e.getValue();
            List<Map<String, Object>> resources = new ArrayList<>();
            for (String res : resourcesByNs.getOrDefault(e.getKey(), Collections.emptySet())) {
                Map<String, Object> rm = new HashMap<>();
                rm.put("accessor", underscore(sanitizeName(res.replace('-', '_'))));
                rm.put("className", toApiName(e.getKey() + "/" + res));
                resources.add(rm);
            }
            m.put("resources", resources);
            nsList.add(m);
        }
        additionalProperties.put("rbNamespaces", nsList);
        objs.put("rbNamespaces", nsList);

        // Zeitwerk inflections: for every autoloaded file whose constant name does not match
        // what the default inflector would camelize from the file name (acronyms like HTTP,
        // IP, API, URL, FA, ...), register an explicit exception on the loader. This covers
        // BOTH models (models/http_config.rb -> HTTPConfig) AND API resource/namespace classes
        // (api/dedicated_cloud/two_fa_whitelist.rb -> DedicatedCloud::TwoFAWhitelist). Emitting
        // an extra inflection is harmless (it just re-asserts the default); OMITTING one breaks
        // autoloading -- so when in doubt, emit. Keyed by file basename and thus GLOBAL to the
        // loader: if two files in different dirs shared a basename with divergent constants the
        // last would win, but generated names are specific enough that this cannot occur.
        Map<String, String> fileToConstant = new LinkedHashMap<>(modelFileToClass);
        // API namespace directories (dir basename -> namespace module) and their resource
        // files (file basename -> leaf class). className is qualified (A::B); Zeitwerk keys on
        // the single path segment, so only the last "::" component matters per basename.
        for (Map<String, Object> ns : nsList) {
            fileToConstant.putIfAbsent((String) ns.get("name"), lastConstantSegment((String) ns.get("className")));
            List<Map<String, Object>> resources = (List<Map<String, Object>>) ns.get("resources");
            for (Map<String, Object> res : resources) {
                fileToConstant.putIfAbsent((String) res.get("accessor"), lastConstantSegment((String) res.get("className")));
            }
        }
        List<Map<String, String>> inflections = new ArrayList<>();
        for (Map.Entry<String, String> e : fileToConstant.entrySet()) {
            String file = e.getKey();
            String klass = e.getValue();
            if (!klass.equals(zeitwerkCamelize(file))) {
                Map<String, String> inflection = new LinkedHashMap<>();
                inflection.put("key", file);
                inflection.put("value", klass);
                inflections.add(inflection);
            }
        }
        additionalProperties.put("zeitwerkInflections", inflections);
        objs.put("zeitwerkInflections", inflections);
        return objs;
    }

    @Override
    public ModelsMap postProcessModels(ModelsMap objs) {
        objs = postProcessModelsEnum(objs);
        for (ModelMap mo : objs.getModels()) {
            CodegenModel cm = mo.getModel();
            // Record file basename -> class name so postProcessSupportingFileData can emit
            // Zeitwerk inflections for acronym names (e.g. http_config.rb -> HTTPConfig).
            modelFileToClass.put(cm.classFilename, cm.classname);
            for (CodegenProperty prop : cm.vars) {
                boolean validated = prop.isEnum || prop.hasValidation || prop.required;
                prop.vendorExtensions.put("x-rb-validated", validated);
                if (prop.defaultValue != null && !"null".equals(prop.defaultValue)) {
                    prop.vendorExtensions.put("x-rb-default", prop.defaultValue);
                }
            }
            if (cm.discriminator != null) {
                cm.vendorExtensions.put("x-rb-discriminator-prop", cm.discriminator.getPropertyBaseName());
                // Templates iterating `{{#discriminator}}` switch the mustache context to the
                // CodegenDiscriminator instance, which carries its own (normally empty)
                // vendorExtensions map; that shadows the model's map and breaks fallback
                // lookups. Mirror the extension onto the discriminator itself so
                // `{{vendorExtensions.x-rb-discriminator-prop}}` resolves inside that section too.
                cm.discriminator.getVendorExtensions().put("x-rb-discriminator-prop", cm.discriminator.getPropertyBaseName());
            }
            // Models that permit additionalProperties (either a typed map --
            // additionalPropertiesType set -- or a bare `additionalProperties: true`/free-form
            // object -- isAdditionalPropertiesTrue) get an overflow store so unknown JSON keys
            // round-trip through from_hash/to_hash instead of being silently dropped. Gated so
            // models without additionalProperties are byte-for-byte unchanged.
            if (cm.getAdditionalPropertiesType() != null || cm.isAdditionalPropertiesTrue) {
                cm.vendorExtensions.put("x-rb-additional-properties", true);
            }
        }
        return objs;
    }

    @Override
    public OperationsMap postProcessOperationsWithModels(OperationsMap objs, List<ModelMap> allModels) {
        objs = super.postProcessOperationsWithModels(objs, allModels);
        OperationMap ops = objs.getOperations();
        for (CodegenOperation co : ops.getOperation()) {
            String rt;
            if (co.returnBaseType == null) {
                rt = "nil";
            } else {
                // returnBaseType is the raw OAS type for primitives (e.g. "integer") but the
                // already-mapped Ruby class name for models (e.g. "Pet"); run it through
                // typeMapping so primitive returns render as `Integer`/`String`/etc.
                String mapped = typeMapping().getOrDefault(co.returnBaseType, co.returnBaseType);
                // Primitives are top-level Ruby core classes (Integer, String, ...) and must
                // NOT be module-qualified; only generated model classes live under moduleName.
                // languageSpecificPrimitives (Boolean, Integer, Float, String, ...) and Object are
                // not Ruby constants Connection#deserialize can dispatch on -- Boolean in
                // particular is not a real Ruby class/module and referencing it bare raises
                // NameError before the HTTP call is even made. Connection#deserialize returns the
                // parsed JSON unchanged when `type` is nil, which is the correct behavior for all
                // of these, so emit `nil` instead of a bare/qualified primitive class reference.
                boolean isPrimitive = languageSpecificPrimitives().contains(mapped) || "Object".equals(mapped);
                // Generated models/enums/oneOf-anyOf wrappers live under `<moduleName>::Models::`
                // (see model.mustache). The return type must include the Models:: segment, otherwise
                // the bare `<moduleName>::<Model>` reference raises NameError the first time an
                // operation is actually invoked (api specs only check reachability, so this stays
                // latent until a real call).
                String baseType = (moduleName == null || isPrimitive) ? mapped : moduleName + "::Models::" + mapped;
                // Connection#deserialize only special-cases Array (`type.is_a?(Array)`); everything
                // else (scalars, maps) is passed through as a bare class reference.
                if (isPrimitive) {
                    // primitive (or array-of-primitive/map-of-primitive): pass the data through
                    // unchanged rather than referencing an undefined/non-dispatchable Ruby type.
                    rt = "nil";
                } else if (co.isMap) {
                    // typed map-of-model deserialization not yet supported; pass through as Hash
                    rt = "nil";
                } else {
                    rt = co.isArray ? "[" + baseType + "]" : baseType;
                }
            }
            co.vendorExtensions.put("x-rb-return-type", rt);
            // Whether any parameter is required, so api.mustache can emit the required-nil
            // guard block followed by a single blank line (and omit both when there are none).
            boolean hasRequired = co.allParams.stream().anyMatch(p -> p.required);
            co.vendorExtensions.put("x-rb-has-required-params", hasRequired);
        }
        return objs;
    }
